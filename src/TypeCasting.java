public class TypeCasting {
    static public void main(String []args){
        int a = 45;
        double b;
        b=a;
        System.out.println(b);
        double num1=19.9;
        int num2;
        num2=(int)num1;
        System.out.println(num2);
        int n1=21;
        int n2=4;
        int result=n1/n2;
        System.out.println(result);

    }
}
